interface fruit {
    name: string,
    color: string,
    isTasty: boolean,
}

const Apple: fruit = {
    name: "Apple",
    color: "Red",
    isTasty: true,
}

const Mango: fruit = {
    name: "Mango",
    color: "Yellow",
    isTasty: true,
}

const Banana: fruit = {
    name: "Banana",
    color: "Yellow",
    isTasty: true,
}
const Kiwi: fruit = {
    name: "kiwi",
    color: "Green",
    isTasty: true,
}

/* Print the information about fruit in object */
console.log("print information about fruits");
console.log("Apple:", Apple);
console.log("Mango:", Mango);
console.log("Banana", Banana);
console.log("Kiwi", Kiwi);

